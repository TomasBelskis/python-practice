import turtle #imports pythons turtle module

window = turtle.Screen() #creates playground(scene/window/canvas) for turtles
trl = turtle.Turtle() #creates turlet assigns to trl

trl.color("red")
trl.forward(50) #moves trl forward 50
trl.left(90) #turns trl 90 degrees
trl.forward(30) #Complete the second side of a rectangle


turtle.mainloop() #waits for user to close the window